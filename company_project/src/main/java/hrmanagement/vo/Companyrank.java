package hrmanagement.vo;

public class Companyrank {
	private int num;
	private int rn_code;
	private String rn_name;
	private int basic_salary;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getRn_code() {
		return rn_code;
	}

	public void setRn_code(int rn_code) {
		this.rn_code = rn_code;
	}

	public String getRn_name() {
		return rn_name;
	}

	public void setRn_name(String rn_name) {
		this.rn_name = rn_name;
	}

	public int getBasic_salary() {
		return basic_salary;
	}

	public void setBasic_salary(int basic_salary) {
		this.basic_salary = basic_salary;
	}

}
