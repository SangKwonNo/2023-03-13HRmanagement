package hrmanagement.vo;

public class Department {
	private int num;
	private int de_code;
	private String de_name;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getDe_code() {
		return de_code;
	}

	public void setDe_code(int de_code) {
		this.de_code = de_code;
	}

	public String getDe_name() {
		return de_name;
	}

	public void setDe_name(String de_name) {
		this.de_name = de_name;
	}

}
