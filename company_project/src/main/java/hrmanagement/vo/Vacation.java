package hrmanagement.vo;

public class Vacation {
	private int num;
	private int em_num;
	private String vacation_type;
	private String vac_start;
	private String vac_end;
	private String memo;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getEm_num() {
		return em_num;
	}
	public void setEm_num(int em_num) {
		this.em_num = em_num;
	}
	public String getVacation_type() {
		return vacation_type;
	}
	public void setVacation_type(String vacation_type) {
		this.vacation_type = vacation_type;
	}
	public String getVac_start() {
		return vac_start;
	}
	public void setVac_start(String vac_start) {
		this.vac_start = vac_start;
	}
	public String getVac_end() {
		return vac_end;
	}
	public void setVac_end(String vac_end) {
		this.vac_end = vac_end;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}



}
